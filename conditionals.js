// kemudian tampilkan pesan apakah mereka telah mencapai lebih dari 15 tahun jika kurang dari 15 tahun keluarkan pesan
// umur anda lebih kecil dari 15 tahun

var age = 17
// isilah bagian if dengan kondisi jika age > 15 tahun maka cetak anda berumur diatas 15 tahun  
